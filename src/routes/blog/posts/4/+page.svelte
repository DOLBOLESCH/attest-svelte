        <p id="article_text">
            Пастор Фриц Шлаг, проповедовавший пацифистские идеи, был арестован гестапо и брошен в берлинскую тюрьму «за распространение пораженческого настроения», где провёл полгода.
        </p>
        <p id="article_text">
            Пастор был освобождён Штирлицем по указанию Шелленберга, который решил использовать связи пастора во влиятельных кругах Швейцарии и Италии. После того, как пастор выполнит свою миссию, Шелленберг, в частности, предлагал ему «устроить автомобильную катастрофу».
        </p>
        <p id="article_text">
            Но Штирлиц, войдя в контакт с пастором, перевербовал его на свою сторону. В результате Шлаг  сыграл ключевую роль в комбинации, задуманной Штирлицем, а не Шелленбергом с Гиммлером.
        </p>
        <p id="article_text">
            В сериале пастор Шлаг показан как высокодуховный, пусть и терзаемый внутренними сомнениями, человек, который продолжает верить в лучшее начало в людях, несмотря на всё то, что ему пришлось вынести. В частности, в разговоре со Штирлицем он говорит, что одинаково помог бы и беглому коммунисту, и нацисту, как как прежде всего они оба являются людьми. Эта его реплика вызывает возражения со стороны Штирлица. Поэтому он и является одним из любимцев зрителей.
        </p>
